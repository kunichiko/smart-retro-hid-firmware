// ===================================================================================
// X68000 マウス機能
// ===================================================================================
// X68000 のキーボードはマウス端子を兼用しており、本体は MSCTRL コマンド
// (0b01000xxM) でマウスデータを要求する。本モジュールはキーボードからの
// MSCTRL 通知を受けて、3 byte のマウスパケットを USART3 (PA18 = MSDATA)
// 経由で本体へ送出する。
//
// プロトコル仕様 §4.3, Appendix C 準拠
// ===================================================================================
#ifndef _X68K_MOUSE_H
#define _X68K_MOUSE_H

#include "../../hid_function.h"
#include <stdint.h>

extern const hid_function_t x68k_mouse_function;

// キーボードモジュールから呼び出す: MSCTRL コマンド (0x40-0x47) の M=1 で送信を起動
void x68k_mouse_handle_msctrl(uint8_t cmd);

#endif
